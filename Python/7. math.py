from math import *
print(pow(5, 2)) # 25.0
print(int(pow(5, 2))) # 25
print(sqrt(25)) # 5.0
print(round(3.2)) # Will print 3 as int
print(int(sqrt(12))) # 3
print(round(sqrt(12))) # 3
print(ceil(9.7)) # 9
print(floor(9.7)) # 10

# Some useful math operators
print(10 * 10) # 100
print(17 / 3) # Returns a float number: 5.66666
print( 17 // 3) # Returns the int floor value: 5
print(17 % 3 ) # Returns int reminder value: 2 
print(5 ** 3) # Basically 5 ^ 3: 125
print(3.5 * 3 - 1) # Multiplication takes precedence
